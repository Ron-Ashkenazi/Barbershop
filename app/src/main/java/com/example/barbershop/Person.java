package com.example.barbershop;

public class Person {
    public String name;
    public String id;
    public String email;
    public String password;
    public String emailid;

    public Person(String name, String id, String email, String password, String emailid) {
        this.name = name;
        this.id = id;
        this.email = email;
        this.password = password;
        this.emailid = emailid;
    }

    public Person() {
    }
}
